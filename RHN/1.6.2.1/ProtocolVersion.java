	private ProtocolVersion(final String name, final TypeVersion type, final int version)
	{
		this.name = name;
		this.type = type;
		this.version = (short) version;
	}
	
	public final String giveName()
	{
		return this.name;
	}
	
	public final TypeVersion giveType()
	{
		return this.type;
	}
	
